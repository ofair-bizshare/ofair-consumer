export const fetchArticleById = async (id: string): Promise<Article | null> => {
  try {
    // In a real application, you would fetch from Supabase using the ID
    // For now, we'll search through our sample articles
    const allArticles = await fetchArticles();
    const article = allArticles.find(article => article.id === id);
    
    return article || null;
  } catch (error) {
    console.error('Error fetching article by ID:', error);
    return null;
  }
};

export const searchArticles = async (query: string): Promise<Article[]> => {
  try {
    const allArticles = await fetchArticles();
    
    if (!query.trim()) {
      return allArticles;
    }
    
    // Simple search implementation
    const searchTerm = query.toLowerCase();
    return allArticles.filter(article => 
      article.title.toLowerCase().includes(searchTerm) || 
      (article.summary && article.summary.toLowerCase().includes(searchTerm)) ||
      article.content.toLowerCase().includes(searchTerm) ||
      (article.author && article.author.toLowerCase().includes(searchTerm)) ||
      (article.category && article.category.toLowerCase().includes(searchTerm))
    );
  } catch (error) {
    console.error('Error searching articles:', error);
    return [];
  }
};

